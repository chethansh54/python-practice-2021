decimalNum = 10
binaryNum = 0b111
octalNum = 0o123
hexadecimalNum = 0xBFF

print(decimalNum)
print(binaryNum)
print(octalNum)
print(hexadecimalNum)

print("======================================================================")
# conversion functions
# bin()
decimalToBinaryNum = bin(decimalNum)
# oct()
decimalToOctalNum = oct(decimalNum)
# hex()
decimalToHexadecimal = hex(decimalNum)

print(decimalToBinaryNum)
print(decimalToOctalNum)
print(decimalToHexadecimal)
